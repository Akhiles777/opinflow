async function main() {
  const client = await prisma.user.findUnique({
    where: { email: "client@test.local" },
    select: { id: true },
  });

  if (!client) {
    throw new Error("Пользователь client@test.local не найден");
  }

  console.log("Создаю опрос...");

  const slug = randomBytes(16).toString("hex");

  const survey = await prisma.survey.create({
    data: {
      creatorId: client.id,
      title: "Опрос удовлетворённости клиентов 2024",
      description:
        "Помогите нам стать лучше — расскажите о вашем опыте работы с нашим сервисом.",
      category: "Маркетинг",
      status: "ACTIVE",
      surveyMode: "SELF_SERVICE",
      publicLinkSlug: slug,
      aiAnalyticsPaid: true,
      questions: {
        create: [
          {
            order: 1,
            type: "SINGLE_CHOICE",
            title: "Как вы в целом оцениваете качество нашего сервиса?",
            required: true,
            options: ["Отлично", "Хорошо", "Удовлетворительно", "Плохо"],
          },
          {
            order: 2,
            type: "MULTIPLE_CHOICE",
            title: "Какими функциями нашего продукта вы пользуетесь?",
            required: true,
            options: [
              "Онлайн-покупки",
              "Личный кабинет",
              "Онлайн-поддержка",
              "Доставка",
              "Рассрочка",
            ],
          },
          {
            order: 3,
            type: "SCALE",
            title:
              "Насколько вы готовы рекомендовать нас своим друзьям и коллегам?",
            description:
              "1 — точно не буду рекомендовать, 10 — однозначно порекомендую",
            required: true,
            settings: { min: 1, max: 10 },
          },
          {
            order: 4,
            type: "RANKING",
            title:
              "Расставьте по приоритету факторы, важные для вас при выборе сервиса:",
            required: true,
            options: [
              "Цена",
              "Качество",
              "Скорость доставки",
              "Поддержка клиентов",
            ],
          },
          {
            order: 5,
            type: "OPEN_TEXT",
            title:
              "Что, по вашему мнению, нам стоит улучшить в первую очередь?",
            required: false,
            settings: { maxLength: 500 },
          },
        ],
      },
    },
    include: { questions: { orderBy: { order: "asc" } } },
  });

  console.log(`Опрос создан: ${survey.id}`);
  const [q1, q2, q3, q4, q5] = survey.questions;

  const passwordHash = await bcrypt.hash("TestPass123!", 10);

  for (let i = 0; i < 20; i++) {
    const ts = Date.now();
    const email = `resp.seed.${ts}.${i}@opinflow-test.local`;
    const referralCode = randomBytes(4).toString("hex").toUpperCase();

    const respondent = await prisma.user.create({
      data: {
        email,
        name: `Респондент ${i + 1}`,
        passwordHash,
        referralCode,
        role: "RESPONDENT",
        status: "ACTIVE",
        emailVerified: new Date(),
      },
    });

    // wallet + profile (same as ensureUserSetup)
    await Promise.all([
      prisma.wallet.create({ data: { userId: respondent.id } }),
      prisma.respondentProfile.create({ data: { userId: respondent.id } }),
    ]);

    const completedAt = new Date(
      Date.now() - Math.floor(Math.random() * 6 * 24 * 60 * 60 * 1000)
    );

    const session = await prisma.surveySession.create({
      data: {
        surveyId: survey.id,
        userId: respondent.id,
        status: "COMPLETED",
        completedAt,
        isValid: true,
        fraudFlags: [],
        answers: {
          create: [
            { questionId: q1.id, value: SINGLE_CHOICES[i] },
            { questionId: q2.id, value: MULTI_CHOICES[i] },
            { questionId: q3.id, value: SCALES[i] },
            { questionId: q4.id, value: RANKINGS[i] },
            { questionId: q5.id, value: OPEN_TEXTS[i] },
          ],
        },
      },
    });

    await prisma.surveyResponse.create({
      data: {
        surveyId: survey.id,
        userId: respondent.id,
        sessionId: session.id,
        moderationStatus: "APPROVED",
      },
    });

    console.log(`  ✓ Ответ ${i + 1}/20`);
  }

  console.log("\n=== Готово ===");
  console.log(`Опрос ID:  ${survey.id}`);
  console.log(`Публичная ссылка: /s/${slug}`);
  console.log(`Страница аналитики: /client/surveys/self-service/${survey.id}`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
